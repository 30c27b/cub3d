#include "cub3d.h"

void		err_exit(char *msg)
{
	ft_putstr_fd(ERR_PREFIX, 2);
	ft_putstr_fd(msg, 2);
	ft_putstr_fd(ERR_SUFFIX, 2);
	exit(EXIT_FAILURE);
}
