#include "cub3d.h"

int			main(void)
{
	t_inst	instance;

	int w = 1920;
	int h = 1080;
	instance = inst_init(map_init(1920, 1080));
	void *img = mlx_xpm_file_to_image(instance.ptr, "./assets/textures/bbt.xpm", &w, &h);
	mlx_put_image_to_window(instance.ptr, instance.win.ptr, img, 0, 0);
	mlx_loop(instance.ptr);
}
