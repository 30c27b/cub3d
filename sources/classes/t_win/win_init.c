#include "cub3d.h"

t_win		win_init(void *mlx_ptr, t_uint64 x, t_uint64 y)
{
	t_win	win;

	win.x = x;
	win.y = y;
	if (!(win.ptr = mlx_new_window(mlx_ptr, x, y, C3D_TITLE)))
		err_exit(ERRTYPE_MLX);
	return (win);
}
